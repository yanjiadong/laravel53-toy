<script>
    var help={
        tab_change:function () {
            var tab_btn = $(".use-help-wrap nav ul li");
            var all_wid = 0;
            for(var i=0;i<tab_btn.length;i++){
                all_wid += $($(".use-help-wrap nav ul li")[i]).outerWidth();
            }
            $(".use-help-wrap nav ul").width(all_wid);
            tab_btn.click(function () {
                $(".tab-page").hide();
                var num = $(this).attr("data-tab");
                $(".tab-page").eq(num).show();
                $(".use-help-main").scrollTop("0");
                tab_btn.removeClass("active");
                $(this).addClass("active");
            });
        }
    };
    $(function () {
        help.tab_change();
    })
</script>
